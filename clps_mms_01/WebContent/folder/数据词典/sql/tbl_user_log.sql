#创建tbl_user_log表
DROP TABLE IF EXISTS tbl_user_log;
create  table tbl_user_log(
log_id BIGINT primary key comment '日志编号',
log_user_name varchar(50) comment '用户名称',
log_up_type int comment '修改类型',
log_up_content varchar(200) comment '修改内容',
log_last_uptime varchar(200) comment '户上次修改日期',
log_up_name varchar(50) comment '用户修改人',
log_remark varchar(200) comment '修改状态',
log_status varchar(10) comment '备注')


