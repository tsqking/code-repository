package com.clps.mms.log.model;

import java.io.Serializable;


/**
 *package:package com.clps.mms.log.booking.model;
 *since:JDK1.8
 *author:andy.wang
 *date:2017年1月21日 下午10:11:12
*/
public class LogBean implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2872705583874908125L;
	private Integer Id; //日志id号
	private String name;//日志名称
	private String type;//修改类型
	private String content;//修改类容
	private String updateName;//修改人员名称
	private String createTime;//修改时间
	
	
	public LogBean() {
		super();
	}
	public LogBean(Integer id, String name, String type, String content, String updateName, String createTime) {
		super();
		Id = id;
		this.name = name;
		this.type = type;
		this.content = content;
		this.updateName = updateName;
		this.createTime = createTime;
	}
	public Integer getId() {
		return Id;
	}
	public void setId(Integer id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getUpdateName() {
		return updateName;
	}
	public void setUpdateName(String updateName) {
		this.updateName = updateName;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	

	
	
}
